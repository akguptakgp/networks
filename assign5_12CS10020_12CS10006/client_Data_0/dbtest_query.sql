drop table if exists STAFF; 
drop table if exists INVESTIGATOR; 
drop table if exists PROJECT; 
drop table if exists FACULTY; 
create table if not exists FACULTY (EC int ,name varchar(20) not NULL,Dept varchar(4) not NULL,Dob Date not NULL,constraint pk_fct PRIMARY KEY (EC));
create table if not exists PROJECT (Code char(4),Type enum('S','C'),Title varchar(50),Sponsor varchar(10),Value int,PI int,constraint pk_prj PRIMARY KEY (Code),constraint fk_prj_fct FOREIGN KEY (PI) references FACULTY(EC));
create table if not exists INVESTIGATOR (Code char(4),EC int,constraint pk_inves PRIMARY KEY (Code,EC),constraint fk_inves_prjt FOREIGN KEY (Code) references PROJECT(Code),constraint fk_inves_fct FOREIGN KEY (EC) references FACULTY(EC));
create table if not exists STAFF(EC int,name varchar(40),Code char(4),Don Date,salary int,constraint PRIMARY KEY(EC),constraint fk_staff_prjt FOREIGN KEY (Code) references PROJECT(Code));  
